package com.example.sharenotes;

public class ListItemsHomeHelper {
    private String head;
    private String desc;

    public ListItemsHomeHelper(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
